	private CommandOperation(int code) {
		this.code = code;
	}

	public static CommandOperation valueOfCode(int code) {
		for (CommandOperation cur : values()) {
			if (cur.code == code) {
				return cur;
			}
		}
		return UNKNOWN;
	}
